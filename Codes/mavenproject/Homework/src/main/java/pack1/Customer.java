package pack1;

public class Customer {
	
	int customer_id;
	String name,surname,age,gender;
	int car_id;
	String debt;
	public Customer(int customer_id, String name, String surname, String age, String gender, int car_id, String debt) {
		super();
		this.customer_id = customer_id;
		this.name = name;
		this.surname = surname;
		this.age = age;
		this.gender = gender;
		this.car_id = car_id;
		this.debt = debt;
	}
	

}
